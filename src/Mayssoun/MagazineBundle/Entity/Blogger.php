<?php

namespace Mayssoun\MagazineBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Blogger
 *
 * @ORM\Table(name="blogger")
 * @ORM\Entity(repositoryClass="Mayssoun\MagazineBundle\Repository\BloggerRepository")
 */
class Blogger
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="code", type="string", length=30, unique=true)
     */
    private $code;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set code
     *
     * @param string $code
     *
     * @return Blogger
     */
    public function setCode($code)
    {
        $this->code = $code;

        return $this;
    }

    /**
     * Get code
     *
     * @return string
     */
    public function getCode()
    {
        return $this->code;
    }
}

